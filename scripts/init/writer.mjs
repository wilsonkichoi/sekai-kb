import { join } from 'node:path';

/* ── TypeScript-literal serializer (deterministic, repo code style) ──────── */

function tsString(s) {
  // \r and U+2028/U+2029 are (parser-vintage) line terminators: raw, they
  // would truncate the single-quoted literal and break place.config.ts at the
  // next build, far from the cause. Answers JSON can carry them; readline
  // cannot.
  return `'${s
    .replace(/\\/g, '\\\\')
    .replace(/'/g, "\\'")
    .replace(/\n/g, '\\n')
    .replace(/\r/g, '\\r')
    .replace(/\u2028/g, '\\u2028')
    .replace(/\u2029/g, '\\u2029')}'`;
}

const BARE_KEY_RE = /^[A-Za-z_$][A-Za-z0-9_$]*$/;

function tsValue(v, indent) {
  const pad = '  '.repeat(indent);
  const padIn = '  '.repeat(indent + 1);
  if (typeof v === 'string') return tsString(v);
  if (typeof v === 'number' || typeof v === 'boolean') return String(v);
  if (Array.isArray(v)) {
    if (v.length === 0) return '[]';
    if (v.every((x) => typeof x !== 'object')) {
      const inline = `[${v.map((x) => tsValue(x, 0)).join(', ')}]`;
      if (inline.length <= 72) return inline;
    }
    const items = v.map((x) => `${padIn}${tsValue(x, indent + 1)},`).join('\n');
    return `[\n${items}\n${pad}]`;
  }
  const entries = Object.entries(v).filter(([, val]) => val !== undefined);
  if (entries.length === 0) return '{}';
  const body = entries
    .map(
      ([k, val]) =>
        `${padIn}${BARE_KEY_RE.test(k) ? k : tsString(k)}: ${tsValue(val, indent + 1)},`,
    )
    .join('\n');
  return `{\n${body}\n${pad}}`;
}

