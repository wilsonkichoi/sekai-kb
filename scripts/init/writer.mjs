const DENYLIST_LOCAL_HEADER = `# genericity-denylist.local.txt — INSTANCE-OWNED additions to the genericity
# gate. Written by \`npm run init\` with this instance's place name; add more
# terms freely (one per line, case-insensitive substring match). Read
# additively by scripts/ci/check-genericity.sh alongside the framework
# denylist, which stays framework-owned so upgrades never conflict.
`;

/** Denylist terms for a place name: the full name plus its no-space form. */
export function denylistTerms(name) {
  const full = name.toLowerCase().trim();
  const collapsed = full.replace(/\s+/g, '');
  return [...new Set([full, collapsed])].filter(Boolean);
}

/* ── The write pass ──────────────────────────────────────────────────────── */

/**
 * Writes all adoption artifacts under `root`. Returns a log of actions taken
 * (one string per artifact) for the transcript.
 */
export function writeInstance(root, cfg) {
  const actions = [];
  const write = (rel, content) => {
    writeFileSync(join(root, rel), content);
    actions.push(`wrote   ${rel}`);
  };

  // place.config.ts — the single writer.
  write('place.config.ts', renderPlaceConfig(cfg));

  // knowledge/: remove demo content, reseed category dirs + INBOX.md.
  rmSync(join(root, 'knowledge'), { recursive: true, force: true });
  for (const cat of cfg.categories) {
    const dir = join(root, 'knowledge', cat.title);
    mkdirSync(dir, { recursive: true });
    writeFileSync(join(dir, '.gitkeep'), '');
  }
  actions.push(
    `seeded  knowledge/{${cfg.categories.map((c) => c.title).join(',')}}/`,
  );
  write('knowledge/INBOX.md', INBOX_MD);

  // CNAME: custom domain only. GitHub Pages default domains must not have one.
  const cname = join(root, 'CNAME');
  if (cfg.place.domain.toLowerCase().endsWith('.github.io')) {
    if (existsSync(cname)) {
      unlinkSync(cname);
      actions.push('removed CNAME (*.github.io domain)');
    } else {
      actions.push('skipped CNAME (*.github.io domain)');
    }
  } else {
    write('CNAME', `${cfg.place.domain}\n`);
  }

  // CLAUDE.md instance header block.
  write('CLAUDE.md', renderClaudeMd(cfg));

  // README.md: the instance repo front page (replaces the template's README).
  write('README.md', renderReadme(cfg));

  // FRAMEWORK-VERSION: the framework version this instance adopted. Written
  // `v`-prefixed to match the release-tag form (sekai-kb-vX.Y.Z) that the
  // /upgrade skill records, so wizard-written and upgrade-written values agree.
  const pkg = JSON.parse(readFileSync(join(root, 'package.json'), 'utf8'));
  write('FRAMEWORK-VERSION', `v${pkg.version}\n`);

  // Local genericity denylist: create or append (idempotent per term). The
  // framework denylist file is never touched.
  const localRel = 'scripts/ci/genericity-denylist.local.txt';
  const localPath = join(root, localRel);
  const existing = existsSync(localPath) ? readFileSync(localPath, 'utf8') : null;
  const have = new Set(
    (existing ?? '')
      .split('\n')
      .map((l) => l.trim())
      .filter((l) => l && !l.startsWith('#')),
  );
  const missing = denylistTerms(cfg.place.name).filter((t) => !have.has(t));
  if (existing === null) {
    write(localRel, `${DENYLIST_LOCAL_HEADER}${missing.join('\n')}\n`);
  } else if (missing.length > 0) {
    const sep = existing.endsWith('\n') ? '' : '\n';
    writeFileSync(localPath, `${existing}${sep}${missing.join('\n')}\n`);
    actions.push(`appended ${localRel} (+${missing.length} term(s))`);
  } else {
    actions.push(`kept    ${localRel} (terms already present)`);
  }

  // Template → instance: drop the marker so the gates scan code trees only.
  const marker = join(root, '.sekai-template');
  if (existsSync(marker)) {
    unlinkSync(marker);
    actions.push('removed .sekai-template (instance mode)');
  }

  // Dev-plugin strip: the framework's own dev-workflow state (config + engineering
  // rules under .agent-toolkit/, and the reference line that points at it) is
  // framework-development state, never adopter content. A fresh instance ships zero
  // dev-plugin state; an adopter who wants the dev workflow runs `dev:setup` to
  // create their own. Strip the tree and the AGENTS.md reference block here;
  // check-init.sh asserts both are absent post-init.
  const agentToolkit = join(root, '.agent-toolkit');
  if (existsSync(agentToolkit)) {
    rmSync(agentToolkit, { recursive: true, force: true });
    actions.push('removed .agent-toolkit/ (dev-plugin state not shipped to adopters)');
  }
  // AGENTS.md is instance-owned but seeded from the framework starter; remove only
  // the sentinel-delimited dev-plugin block, leaving the rest of the file intact.
  const agentsPath = join(root, 'AGENTS.md');
  if (existsSync(agentsPath)) {
    const before = readFileSync(agentsPath, 'utf8');
    const after = before.replace(
      /\n+<!-- dev-plugin:start[\s\S]*?dev-plugin:end -->[ \t]*\n?/,
      '\n',
    );
    if (after !== before) {
      writeFileSync(agentsPath, after);
      actions.push('stripped dev-plugin reference block from AGENTS.md');
    }
  }

  return actions;
}
